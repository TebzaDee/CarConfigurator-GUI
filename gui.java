    public void loadDataFromFile()
    {
       try(FileInputStream fis = new FileInputStream("data.txt"))
       {
           InputStreamReader isr = new InputStreamReader(fis);
           BufferedReader br=new BufferedReader(isr);
           String line;
           
           
           while ((line = br.readLine()) != null)
           {
               String[] parts=line.split(",");
               if(parts.length==5)
               {
                    jTextField1.setText(parts[0].trim());
                    jTextField2.setText(parts[1].trim());
                    jTextField3.setText(parts[2].trim());
                    jTextField4.setText(parts[3].trim());
                    jTextField5.setText(parts[4].trim());
               }
               
           }
           
       }
       catch(IOException e)
       {
           JOptionPane.showMessageDialog(null, "Error reading data from file: " + e.getMessage());
       }
    }
    
    private void saveDataToFile()
    {
        try (FileOutputStream fos = new FileOutputStream("data.txt");
             OutputStreamWriter osw = new OutputStreamWriter(fos);
             BufferedWriter bw = new BufferedWriter(osw)) 
        {

            String line = "Model:" + jTextField1.getText() + "\nColor:" + jTextField2.getText() + "\nExtras to add:" +
                          jTextField3.getText() + "\nName:" + jTextField4.getText() + "\nEmail:" +
                           jTextField5.getText() + "\nOthers:" + jTextArea1.getText();
            bw.write(line);
        }
        
        catch (IOException e) 
        {
            JOptionPane.showMessageDialog(this, "Error saving data to file: " + e.getMessage());
        }
}

    
    

