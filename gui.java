    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        saveDataToFile();
        
      
     
        String carModel = jTextField1.getText();
        String color = jTextField2.getText();
        String extras = jTextField3.getText();
        String buyerName = jTextField4.getText();
        String email = jTextField5.getText();

   
         String otherQueries = jTextArea1.getText();
    
        // Retrieve selected radio button for engine
        String engine = "";
        if (jRadioButton3.isSelected()) {
            engine = "1.4 TFSI";
        } else if (jRadioButton4.isSelected()) {
            engine = "2.0 TFSI";
        } else if (jRadioButton5.isSelected()) {
            engine = "2.0 TDI";
        }
           
        String message = "Car Model: " + carModel + "\nColor: " + color + "\nExtras: " + extras +
                     "\nEngine: " + engine + "\nBuyer Name: " + buyerName + "\nEmail: " + email +
                     "\nOther Queries: " + otherQueries;
            JOptionPane.showMessageDialog(this, message);
            
    }//GEN-LAST:event_jButton1ActionPerformed

